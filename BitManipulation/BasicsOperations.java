package BitManipulation;

public class BasicsOperations {
    public static void main(String[] args) {

        System.out.println("Binary ADD 1 & 0 Output : " + (1 & 0));
        System.out.println("Binary OR 1 | 0  Output : " + (1 | 0));
        System.out.println("Binary XOR 1 ^ 0 Output : " + (1 ^ 0));
        System.out.println("Binary NOT of 1 : " + (~1));
        System.out.println("2<<3 Left Shift : " + (2 << 3));
        System.out.println("2>>3 Right Shift : " + (2 >> 3));

    }

}
